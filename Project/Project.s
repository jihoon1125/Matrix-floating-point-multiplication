	ADD r7, r7, #1; loop count++;
	CMP r7, #31; check loop 30 times
	ADDEQ r0, r0, #120 ; update M1_row
	SUBEQ r1, r1, #120 ; initialize M2_column
	
	BEQ M1_row_update; if loop 30 times, branch to M1_row_change
		
	MOV r6, r1;	update M2_column
	MOV r10, #0; Execution loop count 
	MOV r12, #0; Update floating addition result as 0 (new calculation)	
	
Execution	;Executing state
	ADD r10, r10, #1; loop_count++;
	CMP r10, #31; check loop 30 times
	STREQ r12, [r11], #4 ; store result to r11 and update r11 as r11+4
	SUBEQ r2, r2, #120; initialize r2
	ADDEQ r1, r1, #4; update M2_column	
	BEQ M2_col_update; if loop 30 times, branch to M2_col_update	
	
	LDR r8, [r2], #4; matrix_1 column update
	LDR r9, [r6], #120; matrix_2 row update			
	
	;;checking Nan exception
	AND r3, r8, #0x7FFFFFFF; Don't care nan's sign bit, just check exponent and mantissa	
	LDR r5,=0x7F800000; nan will be greater than 0x7F800000
	CMP r3, r5;	if r8 is nan, r3 will be greater than 0x7F000000
	STRGT r8, [r11], #4;	if r8 is nan, save r8 to r11 directly
	MOVGT r2, r0;	initialize r2 as r0
	ADDGT r1, r1, #4; update M2_column
	BGT M2_col_update;	branch to M2_col_update
	
	
	
	AND r3, r9, #0x7FFFFFFF;  Don't care nan's sign bit, just check exponent and mantissa			
	CMP r3, r5;	if r9 is nan, r3 will be greater than 0x7F800000
	STRGT r9, [r11], #4;	if r9 is nan, save r9 to r11 directly
	MOVGT r2, r0;	initialize r2 as r0
	ADDGT r1, r1, #4; update M2_column
	BGT M2_col_update;	branch to M2_col_update
	
	
	;;check 0 exception
	MOV r3, r8, lsl #1; checking r8 exponent and mantissa
	CMP r3, #0; If r8 is zero, r3 will be 0
	BEQ Execution; If r3==0, branch to Execution
	
	MOV r3, r9, lsl #1; checking r9 exponent and mantissa	
	CMP r3, #0; If r9 is zero, r3 will be 0
	BEQ Execution;	If r3==0, branch to Execution
	
	;; check inf exception	
	MOV r3, r8, lsl #1; checking r8 exponent
	CMP r3, #0xFF000000; If r8 is infinite, r3 will be 0xFF000000
	BEQ Infinite_store; If r8==0xFF000000, branch to Infinite_store
	
	MOV r3, r9, lsl #1	; checking r9 exponent
	CMP r3, #0xFF000000 ; If r9 is infinite, r3 will be 0xFF000000
	BEQ Infinite_store; If r9==0xFF000000, branch to Infinite_store
	
	BL Multiply ; branch to Multiply (return value will in r12)	
	LDMFA sp!, {r0-r11}	; multiple load from stack	
	
	B Execution ; branch to Execution	
	
Infinite_store; Storing registers to stack before Infinite_after_store
	STMFA sp!, {r0-r9}	; multiple store to stack	
	MOV r7, #0; decision count reset
Infinite_after_store; Deciding result value based on r7(Decision count)
	
	AND r4, r8, #0x80000000		; get sign bit
	AND r5, r9, #0x80000000		; get sign bit	
	
	EOR r8, r4, r5; result sign bit
	CMP r8, #0; if r8==0 +inf, else -inf
	ADDEQ r7, r7, #1; if +inf, r7++
	SUBNE r7, r7, #1; if -inf, r7--	
		
	CMP r7, #0;decide result's value
	LDRGT r12, =0x7F800000;if r7>0, +inf
	MOVEQ r12, #0;if r7==0, 0
	LDRLT r12, =0xFF800000;	if r7<0, -inf

Infinite_addr_inc; Finding infinite increasing Matrix address

	ADD r10, r10, #1; loop count++
	CMP r10, #31; Checking looped 30 times
	STREQ r12, [r11], #4; If looped 30 times, put result to r11
	LDMEQFA	sp!, {r0-r9}; multiple load from stack
	MOVEQ r2, r0; matrix_1 column initialize
	ADDEQ r1, r1, #4; update M2_column	
	BEQ M2_col_update; if loop 30 times, branch to M2_col_update	
	
	LDRLT r8, [r2], #4; matrix_1 column update
	LDRLT r9, [r6], #120; matrix_2 row update	
	
	;;checking Nan exception
	AND r3, r8, #0x7FFFFFFF;Don't care nan's sign bit, just check exponent and mantissa	
	LDR r5,=0x7F800000; nan will be greater than 0x7F800000
	CMP r3, r5;	if r8 is nan, r3 will be greater than 0xFF000000	
	STRGT r8, [r11], #4; if r8 is nan, save r8 to r11 directly
	LDMGTFA	sp!, {r0-r9} ; Multiple load from stack
	MOVGT r2, r0;	initialize r2 as r0
	ADDGT r1, r1, #4; update M2_column
	BGT M2_col_update;	branch to M2_col_update
	
	AND r3, r9, #0x7FFFFFFF;Don't care nan's sign bit, just check exponent and mantissa	
	CMP r3, r5;	if r9 is nan, r3 will be greater than 0xFF000000	
	STRGT r9, [r11], #4; if r9 is nan, save r9 to r11 directly
	LDMGTFA	sp!, {r0-r9}; Multiple load from stack
	MOVGT r2, r0;	initialize r2 as r0
	ADDGT r1, r1, #4;  update M2_column
	BGT M2_col_update;	branch to M2_col_update	

	;;check 0 exception
	MOV r3, r8, lsl #1	;checking r8 exponent and mantissa
	CMP r3, #0; If r8 is zero, r3 will be 0
	BEQ Infinite_addr_inc; If r3==0, branch to Infinite_addr_inc
	
	MOV r3, r9, lsl #1	;checking r9 exponent and mantissa
	CMP r3, #0; If r9 is zero, r3 will be 0
	BEQ Infinite_addr_inc; If r3==0, branch to Infinite_addr_inc		

	;; check +inf exception	
	MOV r3, r8, lsl #1	; checking r8 exponent
	CMP r3, #0xFF000000 ; If r8 is infinite, r3 will be 0xFF000000
	BEQ Infinite_after_store; If r8==0xFF000000, branch to Infinite_after_store
	
	MOV r3, r9, lsl #1	; checking r9 exponent
	CMP r3, #0xFF000000 ; If r9 is infinite, r3 will be 0xFF000000
	BEQ Infinite_after_store	; If r9==0xFF000000, branch to Infinite_after_store
	
	B Infinite_addr_inc	; If there is no exception, branch to Infinite_addr_inc

Multiply;; Setting Multiplication
	STMFA sp!, {r0-r11}	; multiple store to stack

	MOV r0, r8 ; put matrix_1 data
	MOV r1, r9 ; put matrix_2 data	
	
	AND r2, r0, #0x80000000		; get sign bit
	AND r3, r1, #0x80000000		; get sign bit	
	
	EOR r8, r2, r3; result sign bit			
	
	MOV r0, r0, lsl #1 		;shift left r0 1 times
	MOV r1, r1, lsl #1		;shift left r1 1 times
	
	MOV r4, r0, lsr #24		;shift right r0 24 times to get exponent
	MOV r5, r1, lsr #24		;shift right r1 24 times to get exponent		
	
	ADD r4, r4, r5; exponent addition
	SUB r4, r4, #127; exponent complement	
	
	MOV r0, r0, lsl #8 		;shift left r0 8 times
	MOV r1, r1, lsl #8		;shift left r1 8 times
	
	MOV r6, r0, lsr #9		;shift right r0 9 times to get mantissa(multiplicand)	
	MOV r7, r1, lsr #9		;shift right r1 9 times to get mantissa(multiplier)		
	
	ADD r6, r6, #0x800000 ;  add 0x800000 to r0's mantissa
	ADD r7, r7, #0x800000 ;  add 0x800000 to r1's mantissa		
	
	MOV r9, #0; It will be U	
		
	MOV r10, r7, lsl #1; It will be X~X[0]	
	MOV r0, #0; Radix-4 state cycle
	LDR r1, =0x00000007; Search which pattern	
booth_op;; booth operation	
	AND r2, r1, r10; Find pattern of Xi+1, Xi, Xi-1	
	
	CMP r2, #0; Shift only
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #1; +1X
	ADDEQ r9, r9, r6 ; +multiplicand	
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #2; +1X		
	ADDEQ r9, r9, r6 ; +multiplicand
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #3; +2X
	ADDEQ r9, r9, r6, lsl #1 ; +2 * multiplicand	
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #4; -2X
	SUBEQ r9, r9, r6, lsl #1 ; -2 * multiplicand	
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #5; -1X
	SUBEQ r9, r9, r6; -multiplicand
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #6; -1X
	SUBEQ r9, r9, r6; -multiplicand
	BEQ booth_shift; branch to shift operation	
	
	CMP r2, #7; shift only	
	BEQ booth_shift; branch to shift operation	
booth_shift;; shifting U, X
	ADD r0, r0, #1; cycle count++;
	
	MOV r9, r9, asr #2; shift twice
		
	;; Xi shift
	AND r2, r10, #6; check what's the value of Xi+1, Xi 
	MOV r2, r2, lsl #22 ; update sign bits
		
	ADD r10, r2, r10, lsr #2; rotate right for shifting twice
	
	CMP r0, #11; check cycled full
	MOVEQ r5, #2; get the bit which will be vanished by next shifting. 
	ANDEQ r5, r9, r5; save it to r5		
	BLE	booth_op; if not full, branch to booth_op again
	
	ADDGT r9, r9, r6; add multiplicand to r9
	LDR r3,=0x00800000; Load 0x00800000 to r3
	CMP r9, r3; compare Final U's value with r3
	SUBGE r11, r9, r3; If r9>=0x00800000, r11=r9-r3
	ADDGE r4, r4, #1; If r9>=0x00800000, r4++ 
	MOVLT r9, r9, lsl #1; If r9<0x00800000, shift left r9 one time 
	SUBLT r9, r9, r3; then, update r9 as r9-0x00800000
	ADDLT r11, r9, r5, lsr #1;	Finally add underflowed U's LSB to r9
		
	ADD r8, r8, r4, lsl #23; sign bit+exponent
	ADD r11, r11, r8; sign bit+exponent+mantissa	
	B Floating_add ; Add result to total value			
	
finish
	MOV pc, #0; if loop 30 times, program end
		
Floating_add
	MOV r0, r11; load r11 to r0;
	MOV r1, r12 ; load r12 to r1	
	
	MOV r2, r0, lsl #1
	
	CMP r2, #0 ; check if r0==0
	MOVEQ r12, r1; if r0==0, result is r1	
	MOVEQ pc, lr; return
	
	MOV r2, r1, lsl #1
	
	CMP r2, #0 ; check if r1==0
	MOVEQ r12, r0; if r1==0, result is r0
	MOVEQ pc, lr;		
	
	AND r2, r0, #0x80000000
	;MOV r2, r0, lsr # 31		;shift right r0 31 times to get sign bit
	AND r3, r1, #0x80000000
	;MOV r3, r1, lsr # 31		;shift right r1 31 times to get sign bit
	
	MOV r0, r0, lsl #1 		;shift left r0 1 times
	MOV r1, r1, lsl #1		;shift left r1 1 times
	MOV r4, r0, lsr #24		;shift right r0 24 times to get exponent
	MOV r5, r1, lsr #24		;shift right r1 24 times to get exponent
	
	MOV r0, r0, lsl #8 		;shift left r0 8 times
	MOV r1, r1, lsl #8		;shift left r1 8 times
	MOV r6, r0, lsr #9		;shift right r0 9 times to get mantissa
	MOV r7, r1, lsr #9		;shift right r1 9 times to get mantissa
	
	ADD r6, r6, #0x800000 ;  add 0x800000 to r0's mantissa
	ADD r7, r7, #0x800000 ;  add 0x800000 to r1's mantissa
	
	CMP r4, r5 	; compare exponent
	MOVGE r10, r4; put greater exponent to r10
	MOVLT r10, r5; put greater exponent to r10
	MOVGE r9, r7 ; if r4>=r5, put r7 into r9, r9 is smaller exponent's mantissa
	MOVLT r9, r6 ; if r4<r5, put r6 into r9
	SUBGE r8, r4, r5; r8=shift num
	SUBLT r8, r5, r4; r8=shift num
	MOV r9, r9, lsr r8; shift right r9 for r9(shift num) times
	
	EOR r11, r2, r3 ; exclusive or r2^r3
	CMP r11 , #0; compare r12 and 0
	BEQ samesign; if r11==0, branch to samesign
	BNE difsign; else, branch to difsign
	
result
	CMP r9, #0; compare r9, 0
	SUBNE r12, r9, #0x800000; put r9-0x800000(mantissa result) to r11 if r9!=0
	MOVEQ r12, r9;  put r9 to r11 if r9==0		
	ADD r12, r12, r10, lsl#23; add r10(exponent_result) and r11 to r11
	ADD r12, r12, r2; add r0 and r11 to r11	
	
	mov pc, lr; return

samesign
	CMP r4, r5 	; compare exponent
	ADDGE r9, r9, r6; if r4>=r5, add r6 to r9
	ADDLT r9, r9, r7; if r4<r5, add r7 to r9
	
	CMP r9, #0x1000000; compare r9 and 0x1000000
	MOVGE r9, r9, lsr #1	;if r9>0x1000000, shift right one time
	ADDGE r10, r10, #1		;if r9>0x1000000, add 1 to r10
	B result ; return
	
difsign
	CMP r4, r5 	; compare exponent
	MOVGE r7, r9; if r4>=r5, move r9 to r7
	MOVLT r6, r9; if r4<r5, move r9 to r6
	
	CMP r3, r2 ; compare sign bit
	SUBGT r9, r7, r6; if r2>r3, r9=r7-r6
	SUBGT r11, r6, r7; r12=~r9
	SUBLT r9, r6, r7; if r2<r3, r9=r6-r7
	SUBLT r11, r7, r6; r12=~r9
	
	CMP r9, #0; check if r9 is plus or minus
	MOVEQ r10, #0; if r9==0, exponent will be 0
	BEQ result ; if r9==0, branch to result
	MOVGT r2, #0; if r9>0, result sign will be 0
	MOVLT r2, #0x80000000; if r9<0, result sign will be 1
	MOVLT r9, r11; if r9<0, mov r12 to r9					
shift
	CMP r9, #0x800000; compare r9 and 0x800000
	MOVLT r9, r9, lsl #1 ; if r9<0x800000, shift left one time
	SUBLT r10, r10, #1 ;   if r9<0x800000, sub 1 from r10
	BLT shift ; if r9<0x800000, branch to shift
	BGE result; if r9>=0x800000, go to result
	
