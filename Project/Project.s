M1_row_update	; Matrix_1 row change

	ADD r4, r4, #1;	loop count++
	
	CMP r4, #31; check whether loop acted 30 times
	BEQ finish;	
	
	MOV r2, r0; update M1_row	
	MOV r7, #0; M2_col_update loop count		
	 
